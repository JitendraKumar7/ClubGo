public class ModalLocation {

    private String id, title, alati, along;

    public ModalLocation() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAlati() {
        return alati;
    }

    public void setAlati(String alati) {
        this.alati = alati;
    }

    public String getAlong() {
        return along;
    }

    public void setAlong(String along) {
        this.along = along;
    }
}
